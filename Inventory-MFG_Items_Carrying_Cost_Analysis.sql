SELECT IM.item_no,  IM.item_desc_1, IM.item_desc_2, IL.std_cost, IL.avg_cost, IL.last_cost, AVG(unit_price), MAX(qty_on_hand)
FROM oelinhst_Sql OL JOIN dbo.imitmidx_sql IM ON IM.item_no = OL.item_no
					 JOIN dbo.Z_IMINVLOC IL ON IL.item_no = IM.item_no
					 JOIN dbo.oehdrhst_sql OH ON oh.inv_no = OL.inv_no
WHERE IM.item_no NOT IN (SELECT item_no 
				  FROM poordlin_sql PL JOIN poordhdr_sql PH ON PH.ord_no = PL.ord_no
				  WHERE (PH.ord_dt > DATEADD(DAY, - 1020, GETDATE())) AND (PL.ord_status <> 'X') AND qty_ordered > 10)
	  AND OH.ord_dt > '01/01/2010'
	  AND unit_price > 0
	  AND qty_on_hand > 0
GROUP BY IM.item_no, IM.item_desc_1, IM.item_desc_2,  IL.std_cost, IL.avg_cost, IL.last_cost

